    // Button Dang Nhap
    private void jButtonDangNhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDangNhapActionPerformed
        if(txtTenDangNhap.getText().length()== 0 || String.valueOf(txtMatKhau.getPassword()).length()==0)    {
            JOptionPane.showMessageDialog(null, "Bạn chưa nhập thông tin tên đăng nhập hoặc mật khẩu", "Thông báo", 1);
        }
        else{
            rs = CheckDangNhap.cLog(txtTenDangNhap.getText(), String.valueOf(txtMatKhau.getPassword()));

            try {
                if(rs.next()) {
                    tenDN = txtTenDangNhap.getText();
                    maNV = rs.getString("manhanvien");
                    Main main = new Main();
                    this.setVisible(false);
                    main.setVisible(true);
                } else{
                    JOptionPane.showMessageDialog(null, "Sai tên đăng nhập hoặc mật khẩu", "Thông báo", 1);
                }
            } catch(SQLException e) {
                JOptionPane.showMessageDialog(null, e, "Thông báo", 1);
            }
        }
    }//GEN-LAST:event_jButtonDangNhapActionPerformed

