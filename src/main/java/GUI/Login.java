    private void jLabelDoiMatKhauMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabelDoiMatKhauMouseClicked
        if(txtTenDangNhap.getText().length()==0 || String.valueOf(txtMatKhau.getPassword()).length()==0)    {
            JOptionPane.showMessageDialog(null, "Bạn chưa nhập thông tin nhập hoặc mật khẩu", "Thông báo", 2);
        }
        else{
            rs = CheckDangNhap.cLog(txtTenDangNhap.getText(), String.valueOf(txtMatKhau.getPassword()));
            
            try{
                if(rs.next()){
                    tenDN = txtTenDangNhap.getText();
                    maNV = rs.getString("manhanvien");
                    ChangePass cp = new ChangePass();
                    this.setVisible(true);
                    cp.setVisible(true);
                } else{
                    JOptionPane.showMessageDialog(null, "Sai tên đăng nhập hoặc mật khẩu", "Thông báo", 1);
                }
            }
            catch(SQLException e){
                JOptionPane.showMessageDialog(null, e, "Thông báo", 1);
            }
        }
    }//GEN-LAST:event_jLabelDoiMatKhauMouseClicked

